package com.chanhhouse.fashion.model;

public class JSPForm {
	private Product product;
	private Underwear underwear;
	private CasualClothes casualClothes;
	private OfficeOutfit officeOutfit;
	private Sleepwear sleepwear;

	public JSPForm() {
	}

	public Product getProduct() {
		return product;
	}

	public void setProduct(Product product) {
		this.product = product;
	}

	public Underwear getUnderwear() {
		return underwear;
	}

	public void setUnderwear(Underwear underwear) {
		this.underwear = underwear;
	}

	public CasualClothes getCasualClothes() {
		return casualClothes;
	}

	public void setCasualClothes(CasualClothes casualClothes) {
		this.casualClothes = casualClothes;
	}

	public OfficeOutfit getOfficeOutfit() {
		return officeOutfit;
	}

	public void setOfficeOutfit(OfficeOutfit officeOutfit) {
		this.officeOutfit = officeOutfit;
	}

	public Sleepwear getSleepwear() {
		return sleepwear;
	}

	public void setSleepwear(Sleepwear sleepwear) {
		this.sleepwear = sleepwear;
	}
	
}
